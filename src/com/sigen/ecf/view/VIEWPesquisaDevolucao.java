    private void setTableModel(List<BeanDevolucao> lsDevolucao) {

        gridCreditoDevolucao.setModel(new CreditoDevolucaoTableModel(lsDevolucao));
        gridCreditoDevolucao.setSelectionModel(new DefaultListSelectionModel() {
            @Override
            public String toString() {
                return "gridPedido";
            }
        });

        //configurarDataTable();
//        FontMetrics fm = gridProduto.getFontMetrics(new java.awt.Font("Arial", 1, 18));
//        gridProduto.setColumnModel(new ProdutoColumnModel(fm));

        if (!lsDevolucao.isEmpty()) {
            gridCreditoDevolucao.setColumnSelectionInterval(1, 1);
            gridCreditoDevolucao.setRowSelectionInterval(0, 0);
            gridCreditoDevolucao.requestFocus();
            configurarDataTable();
        }
    }

    private void pesquisarDevolucao(String cpfCnpj) {
        BeanDevolucao devolucaoPesquisa = new BeanDevolucao();
        devolucaoPesquisa.setCpfCnpj(cpfCnpj);
        setTableModel(DAOFacade.getLsDevolucao(devolucaoPesquisa));
    }

    public BeanDevolucao getCreditoDevolucao() {
        if (gridCreditoDevolucao.getSelectedRow() != -1) {
            CreditoDevolucaoTableModel tableModel = (CreditoDevolucaoTableModel) gridCreditoDevolucao.getModel();
            return tableModel.getSelection(gridCreditoDevolucao.getSelectedRow());
        } else {
            return null;
        }
    }

    private void configurarDataTable() {

        DefaultTableCellRenderer align = new ClienteCellRenderer();

        align.setHorizontalAlignment(SwingConstants.LEFT);

        gridCreditoDevolucao.getColumnModel().getColumn(0).setCellRenderer(align);
        gridCreditoDevolucao.getColumnModel().getColumn(1).setCellRenderer(align);
        gridCreditoDevolucao.setRowHeight(25);
        gridCreditoDevolucao.requestFocus();
        pack();
    }
