    private void configurarComportamentoENTER() {

        gridCreditoDevolucao.addKeyListener(new KeyAdapter() {
            @Override
            public void keyPressed(KeyEvent e) {
                if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                    if (gridCreditoDevolucao.getRowCount() > 0) {
                        cancelado = false;
                        dispose();
                    }
                }
            }
        });
    }
