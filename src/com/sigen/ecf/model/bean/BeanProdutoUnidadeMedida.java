package com.sigen.ecf.model.bean;

import java.math.BigDecimal;

/**
 *
 * @author Administrador
 */
public class BeanProdutoUnidadeMedida {

    private String codProduto;
    private String codUnidade;
    private BigDecimal qtdEmbalagem;
    private boolean unidadePadrao;

    public String getCodProduto() {
        return codProduto;
    }

    public void setCodProduto(String codProduto) {
        this.codProduto = codProduto;
    }

    public String getCodUnidade() {
        return codUnidade;
    }

    public void setCodUnidade(String codUnidade) {
        this.codUnidade = codUnidade;
    }

    public BigDecimal getQtdEmbalagem() {
        return qtdEmbalagem;
    }

    public void setQtdEmbalagem(BigDecimal qtdEmbalagem) {
        this.qtdEmbalagem = qtdEmbalagem;
    }

    public boolean isUnidadePadrao() {
        return unidadePadrao;
    }

    public void setUnidadePadrao(boolean unidadePadrao) {
        this.unidadePadrao = unidadePadrao;
    }
}
